# Storing a value in a variable is called Assignment
# Ex. '+=' --> It adding value and after storing in variable
a = 20

a += 5     # 20 + 5  = 25
print(a)   # 25

a -= 3     # 25 - 3  = 22
print(a)   # 22

a *= 2     # 22 * 2  = 44
print(a)   # 44

a /= 4     # 44 / 4  = 11.0
print(a)   # 11.0

a //= 2    # 11 // 2 = 5.0
print(a)   # 5.0

a %= 3     # 5 % 3   = 2.0
print(a)   # 2.0

a **= 3    # 2 ** 3  = 8.0
print(a)   # 8.0