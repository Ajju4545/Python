# A data type tells Python which type of value a variable is storing.

age = 20
marks = 99.5
name = "Ajay"
is_student = True
numbers = [1, 2, 3, 4]
data = (10, 20, 30)
student = {"name": "Ajay", "age": 20}

print(type(age))
print(type(marks))
print(type(name))
print(type(is_student))
print(type(numbers))
print(type(data))
print(type(student))